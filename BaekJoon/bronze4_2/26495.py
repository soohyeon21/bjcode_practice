# 2, 5, 6에 대해서, "2   \n" 말고 "2\n" 이런식으로 표현해야 한다.
# 예제 출력의 6을 드래그해서 잘 살펴보면 알 수 있다.

import sys

ndict = {"0":"0000 \n0  0 \n0  0 \n0  0 \n0000 ", '1':"   1 \n   1 \n   1 \n   1 \n   1 ",
         "2":"2222 \n   2 \n2222 \n2\n2222 ", "3":"3333 \n   3 \n3333 \n   3 \n3333 ",
         "4":"4  4 \n4  4 \n4444 \n   4 \n   4 ", "5":"5555 \n5\n5555 \n   5 \n5555 ",
         "6":"6666 \n6\n6666 \n6  6 \n6666 ", "7":"7777 \n   7 \n   7 \n   7 \n   7 ",
         "8":"8888 \n8  8 \n8888 \n8  8 \n8888 ", "9":"9999 \n9  9 \n9999 \n   9 \n   9 "}

ipt = sys.stdin.readline().rstrip()
for digit in ipt:
    print(ndict[digit])
    print()
